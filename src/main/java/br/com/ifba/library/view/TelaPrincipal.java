    private void btRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoveActionPerformed
        // TODO add your handling code here:
        int rowIndex = jtTabela.getSelectedRow();
    if (rowIndex == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um livro para remover");
    return;
    }   
        DefaultTableModel model = (DefaultTableModel) jtTabela.getModel();
        Long id = (Long) model.getValueAt(rowIndex, 3);
        model.removeRow(rowIndex);
        bookRepository.deleteById(id);

    }//GEN-LAST:event_btRemoveActionPerformed

