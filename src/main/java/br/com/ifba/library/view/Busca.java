private void resultadoBusca(){
//    book = bookRepository.findById((long)id);
    Optional<Book> optionalBook = bookRepository.findById((long)id);
     book = optionalBook.orElseThrow(() -> new RuntimeException("Livro não encontrado"));
   
    if(book != null) { 
       lbId.setText(Long.toString(book.getId()) );
       lblAutor.setText(book.getAuthor());
       blLivro.setText(book.getTitle());
       lbData.setText(book.getPublicationYear());
    } else if(book == null){
           JOptionPane.showMessageDialog(this, "ID não encontrado");
       }
   }
   

